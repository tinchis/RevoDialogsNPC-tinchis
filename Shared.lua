local function loadConfigFile(path)
    local cfg = LoadResourceFile(GetCurrentResourceName(), path)
    if not cfg then
        print("^1Config file not found: " .. path .. "^0")
        return {}
    end
    local func, err = load(cfg)
    if not func then
        print("^1Error loading config file " .. path .. ": " .. err .. "^0")
        return {}
    end
    return func()
end

